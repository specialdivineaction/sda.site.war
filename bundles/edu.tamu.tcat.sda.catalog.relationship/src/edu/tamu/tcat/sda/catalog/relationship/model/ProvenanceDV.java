package edu.tamu.tcat.sda.catalog.relationship.model;

import java.util.Set;

public class ProvenanceDV
{
   /** The unique identifier of the annotation this provenance describes.
   public String annotationId;

   /** The string-valued URIs associated with the creators of the associated annotation. */
   public Set<String> creatorUris;

   /** Date created in ISO 8601 format */
   public String dateCreated;

   /** Date modified in ISO 8601 format */
   public String dateModified;
}
